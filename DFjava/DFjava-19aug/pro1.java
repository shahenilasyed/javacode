class LU {

	public static void main(String... args) {

		//Take a char input
		char ch = 'v';

		//Check for Upper case
		if(ch >= 65 && ch <= 90)
			System.out.println(ch + " is an Upper Case Alphabet");
		//Check for Lower Case
		else if(ch >= 97 && ch <= 122)
			System.out.println(ch + " is an Lower Case ALphabet");
	}
}